@router.post("/{email_id}/action")
async def email_action(
    email_id: str,
    payload:  EmailActionRequest,
    current_user: dict = Depends(get_current_user)
):
    if payload.operation not in OP_MAP:
        raise HTTPException(status_code=400, detail="Invalid operation")
    email      = current_user.get("email")
    password   = current_user.get("password")
    user_id    = current_user.get("id")
    message_id = payload.message_id or ""

    if payload.operation == "read":
        await zimbra_client.mark_as_read(email, password, email_id,
                                         folder=payload.folder or "INBOX")
        await email_state_repo.upsert_read(user_id, message_id, True)
    elif payload.operation == "unread":
        await email_state_repo.upsert_read(user_id, message_id, False)
    elif payload.operation in ("star", "unstar"):
        is_star = payload.operation == "star"
        await zimbra_client.star_email(email, password, email_id, is_star,
                                       folder=payload.folder or "INBOX")
        await email_state_repo.upsert_starred(user_id, message_id, is_star)
    elif payload.operation in ("delete", "trash"):
        src = payload.folder or "INBOX"
        await zimbra_client.move_to_trash(email, password, email_id, source=src)
    elif payload.operation == "trash_from_spam":
        await zimbra_client.move_to_trash(email, password, email_id, source="SPAM")
    elif payload.operation == "spam_from_trash":
        await zimbra_client.move_to_spam(email, password, email_id, source="TRASH")
    elif payload.operation == "spam":
        src = payload.folder or "INBOX"
        await zimbra_client.move_to_spam(email, password, email_id, source=src)
    elif payload.operation == "not_spam":
        await zimbra_client.move_from_spam(email, password, email_id)
    elif payload.operation == "restore_from_trash":
        await zimbra_client.move_from_trash(email, password, email_id)
    elif payload.operation == "permanent_delete":
        await zimbra_client.permanent_delete(email, password, email_id)
        await email_state_repo.delete_state(user_id, message_id)

    _ACTION_LABEL = {
        "read": "read", "unread": "unread",
        "star": "starred", "unstar": "unstarred",
        "delete": "trashed", "trash": "trashed", "trash_from_spam": "trashed",
        "spam": "spam", "spam_from_trash": "spam",
        "not_spam": "not_spam", "restore_from_trash": "restored",
        "permanent_delete": "permanently_deleted",
    }
    # Log the folder where the email now lives so admin can fetch it later
    _DEST_FOLDER = {
        "trash": "TRASH", "delete": "TRASH", "trash_from_spam": "TRASH",
        "spam": "SPAM", "spam_from_trash": "SPAM",
        "restore_from_trash": "INBOX", "not_spam": "INBOX",
    }
    dest_folder  = _DEST_FOLDER.get(payload.operation, payload.folder or "INBOX")
    action_label = _ACTION_LABEL.get(payload.operation, payload.operation)
    await activity_log_repo.log(
        user_id=user_id, action=action_label,
        email_id=email_id, message_id=message_id or None,
        folder=dest_folder
    )
    return {"status": "ok", "operation": payload.operation, "email_id": email_id}
