# Extensions blocked for security — executables and script files
_BLOCKED_EXTENSIONS = {
    '.exe', '.bat', '.sh', '.cmd', '.ps1', '.vbs', '.jar',
    '.msi', '.scr', '.pif', '.com', '.dll', '.reg', '.hta',
    '.wsf', '.php', '.py', '.rb', '.pl', '.cgi',
}
_MAX_FILE_BYTES  = 10 * 1024 * 1024   # 10 MB per file
_MAX_TOTAL_BYTES = 25 * 1024 * 1024   # 25 MB total

router = APIRouter(prefix="/api/emails", tags=["emails"])

# Global set of message_ids currently being summarized — prevents duplicate work
# when the frontend polls GET /api/emails every few seconds.
_summarizing: set[str] = set()

# System senders generated by Zimbra/OS — filtered out of the interface
# (they remain in Zimbra and are visible in the Zimbra admin panel)
_SYSTEM_SENDER_PREFIXES = ("root@", "zimbra@", "postmaster@", "mailer-daemon@", "admin-noreply@")

def _is_system_email(email_dict: dict) -> bool:
    sender = (email_dict.get("from") or "").lower().strip()
    # strip display name if present: "Zimbra <zimbra@host>" → "zimbra@host"
    if "<" in sender:
        sender = sender.split("<")[-1].rstrip(">").strip()
    return any(sender.startswith(p) for p in _SYSTEM_SENDER_PREFIXES)


class SendEmailRequest(BaseModel):
    to:          str
    subject:     str
    body:        str
    original_id: Optional[str] = None
